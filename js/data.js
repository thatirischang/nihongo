// 把所有「需要生成音频」的罗马字汇总（去重）
function _collectAllRomaji() {
  const set = new Set();
  for (const row of GOJUON) for (const c of row) if (c) set.add(c.r);
  for (const c of ARCHAIC) set.add(c.r);
  for (const row of DAKUON) for (const c of row) set.add(c.r);
  for (const row of HANDAKUON) for (const c of row) set.add(c.r);
  for (const row of YOUON) for (const c of row) set.add(c.r);
  return Array.from(set).sort();
}

const ALL_ROMAJI = _collectAllRomaji();
